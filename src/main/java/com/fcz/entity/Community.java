package com.fcz.entity;


public class Community {
    private Integer communityId;

    private String communityAddress;

    private String communityAdmin;

    private String communityAdminPhone;

    private String communityImage;

    private String communityLogo;

    private String communityName;

    private String communityServicePhone;

    public Integer getCommunityId() {
        return communityId;
    }

    public void setCommunityId(Integer communityId) {
        this.communityId = communityId;
    }

    public String getCommunityAddress() {
        return communityAddress;
    }

    public void setCommunityAddress(String communityAddress) {
        this.communityAddress = communityAddress;
    }

    public String getCommunityAdmin() {
        return communityAdmin;
    }

    public void setCommunityAdmin(String communityAdmin) {
        this.communityAdmin = communityAdmin;
    }

    public String getCommunityAdminPhone() {
        return communityAdminPhone;
    }

    public void setCommunityAdminPhone(String communityAdminPhone) {
        this.communityAdminPhone = communityAdminPhone;
    }

    public String getCommunityImage() {
        return communityImage;
    }

    public void setCommunityImage(String communityImage) {
        this.communityImage = communityImage;
    }

    public String getCommunityLogo() {
        return communityLogo;
    }

    public void setCommunityLogo(String communityLogo) {
        this.communityLogo = communityLogo;
    }

    public String getCommunityName() {
        return communityName;
    }

    public void setCommunityName(String communityName) {
        this.communityName = communityName;
    }

    public String getCommunityServicePhone() {
        return communityServicePhone;
    }

    public void setCommunityServicePhone(String communityServicePhone) {
        this.communityServicePhone = communityServicePhone;
    }
}